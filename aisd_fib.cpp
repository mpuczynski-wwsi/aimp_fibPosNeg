#include<iostream>
#include<cstdlib>

using namespace std;


int * fibIter(int n){
    long long a = 0, b = 1;
    int fibArr[n];

    for (int i = 0; i < n; i++){
        b += a;
        a = b-a;
        fibArr[i] = a; 
        fibArr[i+1] = b; 
    }

}

